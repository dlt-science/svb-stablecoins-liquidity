def find_files(directory, start_date, end_date, ext=".csv",
               date_fmt="%Y-%m-%d %H-%M-%S", year="2023"):
    """Glob for data files and filter to [start_date, end_date].

    Returns an OrderedDict  {datetime -> filepath}  sorted by date.
    """
    pattern = os.path.join(directory, f"**/*{ext}")
    paths = glob.glob(pattern, recursive=True)

    start_dt = datetime.strptime(start_date, "%Y-%m-%d")
    end_dt = datetime.strptime(end_date, "%Y-%m-%d")

    result = {}
    for p in paths:
        if year not in p:
            continue
        basename = os.path.basename(p).split(".")[0]
        try:
            dt = datetime.strptime(basename, date_fmt)
        except ValueError:
            continue
        if start_dt <= dt <= end_dt:
            result[dt] = p

    return OrderedDict(sorted(result.items()))


