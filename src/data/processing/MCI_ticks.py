def prepare_data(tick_df, pools_df, pairs_filter=None):
    """Merge tick-level data with daily pool TVL for one date.

    Args:
        tick_df:       Tick-level parquet data for a single timestamp.
        pools_df:      Daily pool TVL from getPoolData_daily.py (one row per pool).
        pairs_filter:  Optional list of trading_pair strings to keep.

    Steps:
      - Build a 'trading_pair' key on the tick side and inner-join with pool TVL.
      - Normalise token symbol names (some come as full names).
      - Drop ignored pools (from pools_config).
      - Filter USDT pools with USDC-backed counterparts.
      - Optionally keep only pairs in *pairs_filter*.
    """
    # Build the join key on the tick side:  "USDCWETH500"
    tick_df = tick_df.copy()
    tick_df["pool_address"] = tick_df["pool_address"].str.lower()
    tick_df["trading_pair"] = (
        tick_df["token0_symbol"] + tick_df["token1_symbol"]
        + tick_df["fee_tier"].astype(str)
    )

    # Keep only TVL + stablecoin from pool data, keyed by trading_pair
    pool_tvl = pools_df[["trading_pair", "totalValueLockedUSD", "stablecoin"]].copy()
    pool_tvl = pool_tvl.drop_duplicates(subset=["trading_pair"], keep="first")

    # Inner join — only pools present in both datasets survive
    merged = tick_df.merge(pool_tvl, on="trading_pair", how="inner")

    # Some on-chain token symbols are full names; normalise to ticker
    name_to_ticker = {"USD Coin": "USDC", "Tether USD": "USDT"}
    merged["token0_symbol"] = merged["token0_symbol"].replace(name_to_ticker)
    merged["token1_symbol"] = merged["token1_symbol"].replace(name_to_ticker)

    # Filter specific pairs if requested
    if pairs_filter:
        merged = merged[merged["trading_pair"].isin(pairs_filter)]

    # Remove pools marked as inactive in the central config
    ignored = get_pools_to_ignore()
    merged = merged[~merged["pool_address"].isin(ignored)]

    # Get pools to use
    use_pools = get_active_pools()
    merged = merged[merged["pool_address"].isin(use_pools)]

    # Remove USDT pools whose counterpart token is USDC-backed
    usdc_backed_mask = merged.apply(
        lambda r: is_usdc_backed_usdt_pair(
            r["token0_symbol"], r["token1_symbol"], r["stablecoin"]
        ),
        axis=1,
    )
    merged = merged[~usdc_backed_mask]

    return merged


