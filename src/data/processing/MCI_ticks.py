def parse_args():
    p = argparse.ArgumentParser(
        description="Compute MCI from tick data (simplified v2)")

    p.add_argument("--filtered_files", default="./../../../data/filtered",
                   help="Base directory for filtered / output data")

    p.add_argument("--ticks_files", default="./../../../data/pools/rpc",
                   help="Directory containing tick parquet files")

    # Pool TVL source (overridden automatically when --hourly is used)
    p.add_argument("--pools_files", default=None,
                   help="Directory containing pool TVL CSVs "
                        "(default: pools_daily or pools_hourly based on mode)")

    p.add_argument("--hourly", action="store_true",
                   help="Use hourly pool TVL from pools_hourly "
                        "(default: daily from pools_daily)")

    p.add_argument("--start_date", default="2023-02-28")
    p.add_argument("--end_date", default="2023-04-01")
    p.add_argument("--num_cpus", type=int, default=2,
                   help="CPUs: -1=all, 1=1/3, 2=2/3, other=1/2")

    return p.parse_args()


def pick_cpu_count(requested):
    """Translate the --num_cpus flag into an actual process count."""
    total = cpu_count()
    mapping = {-1: total, 1: max(1, total // 3), 2: max(1, total * 2 // 3)}
    n = mapping.get(requested, max(1, total // 2))
    print(f"Using {n} / {total} CPUs")
    return n


def main():
    args = parse_args()
    hourly = args.hourly

    # Default pool TVL directory based on mode
    if args.pools_files is None:
        if hourly:
            args.pools_files = os.path.join(args.filtered_files, "pools_hourly")
        else:
            args.pools_files = os.path.join(args.filtered_files, "pools_daily")

    # ---- Discover files for the date range ----
    tick_files = find_files(
        args.ticks_files, args.start_date, args.end_date,
        ext=".parquet", date_fmt="%Y-%m-%d %H-%M-%S",
    )

    if hourly:
        # Hourly mode: match each tick file to its exact hourly pool TVL CSV
        pool_files = find_files(
            args.pools_files, args.start_date, args.end_date,
            ext=".csv", date_fmt="%Y-%m-%d %H-%M-%S",
        )
        pool_files = {k.strftime("%Y-%m-%d %H-%M-%S"): v
                      for k, v in pool_files.items()}
    else:
        # Daily mode: match each tick file to its corresponding daily pool CSV
        pool_files = find_files(
            args.pools_files, args.start_date, args.end_date,
            ext=".csv", date_fmt="%Y-%m-%d",
        )
        pool_files = {k.strftime("%Y-%m-%d"): v for k, v in pool_files.items()}

    if not tick_files:
        raise FileNotFoundError(
            f"No tick parquet files found in {args.ticks_files} "
            f"for {args.start_date} .. {args.end_date}"
        )

    # ---- Build work items (one per timestamp) ----
    # Match tick files to pool TVL files by datetime (hourly) or date (daily)
    pool_key_fmt = "%Y-%m-%d %H-%M-%S" if hourly else "%Y-%m-%d"
    work = []
    for dt, tfile in tick_files.items():
        key = dt.strftime(pool_key_fmt)
        if key not in pool_files:
            print(f"WARNING: No pool TVL file for {key}, skipping")
            continue
        work.append(
            (dt, args.start_date, args.end_date, tfile,
             pool_files[key], args.filtered_files)
        )

    # ---- Run with multiprocessing ----
    n_cpus = pick_cpu_count(args.num_cpus)

    with Pool(processes=n_cpus) as pool:
        results = list(tqdm(
            pool.imap(process_one_date, work),
            total=len(work),
            desc="MCI processing",
        ))

    # ---- Concatenate all dates and save final output ----
    final_df = pd.concat(results)
    out_path = os.path.join(args.filtered_files, "mci_stats.parquet")
    final_df.to_parquet(out_path, index=False, engine="fastparquet")
    final_df.to_csv(out_path.replace(".parquet", ".csv"), index=False)
    print(f"Saved final MCI stats to {out_path}")


if __name__ == "__main__":
    t0 = time.time()
    main()
    print(f"Done in {time.time() - t0:.1f}s")
