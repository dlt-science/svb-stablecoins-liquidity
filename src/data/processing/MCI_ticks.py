def compute_mci_for_level(ticks_df, level, mid_price, tick_spacing,
                          decimals0, decimals1, sqrt_price_x96,
                          pair, date_str, flip_flag):
    """Compute buy MCI, sell MCI, average, and imbalance for one level.

    Returns (mci_avg, mci_imbalance, buy_mci, sell_mci).
    All zeros when depth is insufficient.
    """
    ticks_df = ticks_df.reset_index(drop=True)

    buy_mci = an.mci(
        is_bid=False, level=level, mid_price=mid_price,
        ticks=ticks_df, tick_spacing=tick_spacing,
        decimals0=decimals0, decimals1=decimals1,
        sqrt_price_x96=sqrt_price_x96,
        pair=pair, date_val=date_str, flip_flag=flip_flag,
    )

    sell_mci = an.mci(
        is_bid=True, level=level, mid_price=mid_price,
        ticks=ticks_df, tick_spacing=tick_spacing,
        decimals0=decimals0, decimals1=decimals1,
        sqrt_price_x96=sqrt_price_x96,
        pair=pair, date_val=date_str, flip_flag=flip_flag,
    )

    # Both zero means depth was insufficient at this level
    if buy_mci == 0 and sell_mci == 0:
        return 0.0, 0.0, 0.0, 0.0

    avg = (buy_mci + sell_mci) / 2
    imbalance = (buy_mci - sell_mci) / (buy_mci + sell_mci)
    return avg, imbalance, buy_mci, sell_mci


def compute_mci_all_pools(merged_df, date_str, levels=LEVELS,
                          base_tokens=BASE_TOKENS):
    """Compute MCI for every pool in *merged_df* across all levels.

    Returns a DataFrame with one row per pool (columns = DETAIL_COLUMNS).
    """
    rows = []

    for pair in merged_df["trading_pair"].unique():
        sub = merged_df[merged_df["trading_pair"] == pair]
        first = sub.iloc[0]

        mid_price = Decimal(first["price_in_stablecoin"])
        token0 = first["token0_symbol"]
        token1 = first["token1_symbol"]
        decimals0 = int(first["token0_decimals"])
        decimals1 = int(first["token1_decimals"])
        sqrt_px96 = int(first["sqrt_price_x96"])
        tick_sp = first["tick_spacing"]
        fee = int(first["fee_tier"])
        tvl = float(first["totalValueLockedUSD"])

        # Old: hourly volume from Messari (no longer needed)
        # vol = float(first["hourlyVolumeUSD"])

        # flip_flag: True when token1 is NOT the stablecoin
        flip = token1 not in base_tokens
        stablecoin = token0 if flip else token1

        print(f"  {pair}  (TVL ${tvl:,.0f})")

        mcis, imbs, buys, sells = [], [], [], []
        for level in levels:
            avg, imb, buy, sell = compute_mci_for_level(
                sub, level, mid_price, tick_sp,
                decimals0, decimals1, sqrt_px96,
                pair, date_str, flip,
            )
            mcis.append(avg)
            imbs.append(imb)
            buys.append(buy)
            sells.append(sell)

        rows.append(
            [stablecoin, token0, token1, fee, mid_price, tvl]
            + mcis + buys + sells + imbs
        )

        # Old row format (included vol):
        # rows.append(
        #     [stablecoin, token0, token1, fee, mid_price, vol, tvl]
        #     + mcis + buys + sells + imbs
        # )

    return pd.DataFrame(rows, columns=DETAIL_COLUMNS)


# ===================================================================
# Weighted averages across pools for each stablecoin
# ===================================================================

def compute_weighted_averages(detail_df, sizes=SIZES,
                              base_tokens=BASE_TOKENS):
    """TVL-weighted average of MCI columns, grouped by stablecoin.

    Pools in POOLS_SKIP_IN_WEIGHTED_AVG are excluded from the average
    (but kept in the detailed output).
    """
    metric_cols = (
        [f"MCI_{s}" for s in sizes]
        + [f"Buy_MCI_{s}" for s in sizes]
        + [f"Sell_MCI_{s}" for s in sizes]
        + [f"MCI_imbalance_{s}" for s in sizes]
    )

    df = detail_df.copy().fillna(0)

    # Build pool id label for filtering
    df["pool_id"] = (
        df["token0_symbol"] + df["token1_symbol"] + df["fee_tier"].astype(str)
    )
    df = df[~df["pool_id"].isin(POOLS_SKIP_IN_WEIGHTED_AVG)]

    # Weighted average per stablecoin, weighted by TVL
    weighted = pd.DataFrame(columns=metric_cols, index=base_tokens)
    for col in metric_cols:
        weighted[col] = df.groupby("stablecoin").apply(
            lambda g: np.average(g[col], weights=g["totalValueLockedUSD"])
        )

    return weighted


