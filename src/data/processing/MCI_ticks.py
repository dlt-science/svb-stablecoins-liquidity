def process_one_date(args_tuple):
    """Process a single date: load data, compute MCI, save results.

    Designed to be called via multiprocessing.Pool.imap().  Accepts a single
    tuple so it's compatible with pool.imap / pool.map.

    Returns the weighted-averages DataFrame for this date.
    """
    (date_dt, start_date, end_date,
     tick_file, pool_file, output_dir) = args_tuple

    # date_str is used for filenames and display (e.g. "2023-03-01 00-00-00")
    date_str = date_dt.strftime("%Y-%m-%d %H-%M-%S")

    # Output paths
    mci_dir = os.path.join(output_dir, "mci_ticks")
    detail_dir = os.path.join(output_dir, "mci_detailed_ticks")
    os.makedirs(mci_dir, exist_ok=True)
    os.makedirs(detail_dir, exist_ok=True)

    basename = os.path.basename(tick_file).split(".")[0]
    mci_path = os.path.join(mci_dir, f"{basename}.parquet")
    detail_path = os.path.join(detail_dir, f"{basename}.parquet")

    # Skip if already computed
    if os.path.exists(mci_path):
        print(f"Already done: {basename}")
        return pd.read_parquet(mci_path, engine="fastparquet")

    # ---- Load data ----
    print(f"Loading {tick_file} ...")
    tick_df = pd.read_parquet(tick_file, engine="fastparquet")

    # Load daily pool TVL (from getPoolData_daily.py)
    pool_df = pd.read_csv(pool_file, engine="c", usecols=POOL_CSV_COLS)

    # Old: load Messari hourly CSV with different columns
    # pool_df = pd.read_csv(pool_file, engine="c", usecols=[
    #     "date", "totalValueLockedUSD", "hourlyVolumeUSD",
    #     "pool_address", "trading_pair", "stablecoin",
    # ])

    # ---- Merge tick + pool data ----
    merged = prepare_data(tick_df, pool_df)

    # Old: passed date_dt for hourly filtering of Messari data
    # merged = prepare_data(tick_df, pool_df, date_dt)

    if merged.empty:
        print(f"WARNING: No data after merge for {date_str}, skipping")
        return pd.DataFrame()

    # ---- Compute MCI for every pool ----
    print(f"Computing MCI for {date_str} ...")
    detail_df = compute_mci_all_pools(merged, date_str)

    # ---- Save detailed per-pool results ----
    detail_df.to_parquet(detail_path, index=False, engine="fastparquet")
    detail_df.to_csv(detail_path.replace(".parquet", ".csv"), index=False)

    # ---- Compute TVL-weighted averages per stablecoin ----
    weighted = compute_weighted_averages(detail_df)
    weighted["date"] = date_str
    weighted = weighted.reset_index().rename(columns={"index": "stablecoin"})

    # ---- Save weighted averages ----
    weighted.to_parquet(mci_path, index=False, engine="fastparquet")
    weighted.to_csv(mci_path.replace(".parquet", ".csv"), index=False)

    return weighted


